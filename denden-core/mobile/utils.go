// Package mobile provides GoMobile-compatible wrappers for the DenDen client.
// This file contains utility functions.
package mobile

// escapeJSON escapes special characters in a string for JSON
func escapeJSON(s string) string {
	result := ""
	for _, c := range s {
		switch c {
		case '"':
			result += `\"`
		case '\\':
			result += `\\`
		case '\n':
			result += `\n`
		case '\r':
			result += `\r`
		case '\t':
			result += `\t`
		default:
			result += string(c)
		}
	}
	return result
}
